#include <unistd.h>

void	ft_print_comb(void)
{
	int	cf1;
	int	cf2;
	int	cf3;

	cf1 = '0';
	while (cf1 <= '7')
	{	
		cf2 = cf1 + 1 ;
		while (cf2 <= '8')
		{	
			cf3 = cf2 + 1;
			while (cf3 <= '9')
			{	
				write(1, &cf1, 1);
				write(1, &cf2, 1);
				write(1, &cf3, 1);
				if (cf1 != '7')
					write(1, ", ", 2);
				cf3 = cf3 + 1;
			}
			cf2 = cf2 + 1;
		}
		cf1 = cf1 + 1;
	}
}
