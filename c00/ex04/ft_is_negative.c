#include <unistd.h>

void	ft_is_negative(int n)
{
	int	neg;
	int	pos;

	neg = 78;
	pos = 80;
	if (n >= 0)
	{
		write(1, &pos, 1);
	}
	else
	{
		write(1, &neg, 1);
	}
}
